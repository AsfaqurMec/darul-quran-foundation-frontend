export function buildStaticBlogs(): BlogDetailData[] {
  const list: BlogDetailData[] = [];
  for (let i = 0; i < 40; i++) {
    const b = baseBlogs[i % baseBlogs.length];
    const newId = `${b.id}-${i}`;
    list.push({ 
      ...b, 
      id: newId, 
      href: `/blog/${newId}`,
      relatedPosts: [
        baseBlogs[(i + 1) % baseBlogs.length].id + `-${i + 1}`,
        baseBlogs[(i + 2) % baseBlogs.length].id + `-${i + 2}`,
      ],
    });
  }
  return list;
}

export function getBlogDetail(id: string): BlogDetailData | null {
  const all = buildStaticBlogs();
  return all.find((blog) => blog.id === id || blog.href?.includes(id)) || null;
}

export function getLatestBlogs(count: number ): BlogDetailData[] {
  return baseBlogs.slice(0, count).map((blog, idx) => ({
    ...blog,
    href: `/blog/${blog.id}`,
  }));
}

